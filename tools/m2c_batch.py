#!/usr/bin/env python3
"""
m2c_batch.py — Batch-convert all unmatched FSA functions to C via m2c.

Usage:
  python tools/m2c_batch.py               # convert all ~6000 functions
  python tools/m2c_batch.py --test 20     # dry-run on first 20 functions
  python tools/m2c_batch.py --jobs 8      # parallel workers (default: cpu count)
  python tools/m2c_batch.py --out src/nonmatch  # output directory (default)

Output layout:
  src/nonmatch/seg_XXXXXXXX.c   — one file per ~50-function address-contiguous chunk
  src/nonmatch/_declarations.h  — forward declarations for all converted functions
"""

import argparse
import os
import re
import subprocess
import sys
from concurrent.futures import ProcessPoolExecutor, as_completed
from pathlib import Path

# --- configuration -----------------------------------------------------------
REPO_ROOT   = Path(__file__).resolve().parent.parent
ASM_DIR     = REPO_ROOT / "build" / "G4SE01" / "asm"
OUT_DIR     = REPO_ROOT / "src" / "nonmatch"
M2C_BIN     = Path.home() / ".local/share/uv/tools/m2c/bin/m2c"
M2C_TARGET  = "ppc"
CHUNK_SIZE  = 50          # functions per output .c file
# -----------------------------------------------------------------------------


def find_asm_files() -> list[Path]:
    """Return all auto-generated asm files, sorted by the address in their name."""
    files = sorted(
        (p for p in ASM_DIR.glob("auto_*_text.s")
         if re.search(r'_([0-9A-Fa-f]{8})_text', p.name)),
        key=lambda p: int(re.search(r'_([0-9A-Fa-f]{8})_text', p.name).group(1), 16)
    )
    return files


def convert_one(asm_path: Path) -> tuple[Path, str, str]:
    """Run m2c on a single asm file. Returns (path, stdout, stderr)."""
    result = subprocess.run(
        [str(M2C_BIN), "--target", M2C_TARGET, str(asm_path)],
        capture_output=True, text=True
    )
    return asm_path, result.stdout, result.stderr


def addr_from_name(p: Path) -> int:
    m = re.search(r'_([0-9A-Fa-f]{8})_text', p.name)
    return int(m.group(1), 16) if m else 0


def chunk_files(files: list[Path], chunk_size: int) -> list[list[Path]]:
    """Split file list into address-contiguous chunks of ~chunk_size functions."""
    chunks, cur = [], []
    for f in files:
        cur.append(f)
        if len(cur) >= chunk_size:
            chunks.append(cur)
            cur = []
    if cur:
        chunks.append(cur)
    return chunks


def fix_unknown_types(c_src: str) -> str:
    """Replace m2c's '?' placeholder type with compilable types.

    m2c writes `extern ? fn_XXXXXXXX;` when a function address is used as a
    value. We must NOT write `extern u32 fn_XXXXXXXX;` because that declares
    it as a variable — which conflicts with any later function definition.
    Instead, emit a proper forward declaration `void fn_XXXXXXXX();`.
    Data labels (lbl_*) stay as u32.
    """
    # Forward-declare function-like names: fn_*, dtor_*, ctor_*
    def fn_extern(m):
        name = m.group(1)
        return f"void {name}();"
    c_src = re.sub(
        r'extern \? ((?:fn|dtor|ctor)_[0-9A-Fa-f]+)\s*;',
        fn_extern, c_src
    )
    # char* for unknown pointers: allows pointer arithmetic (arg+0x10),
    # passes implicitly to void* params, avoids CodeWarrior void* restrictions.
    c_src = re.sub(r'\?\s*\*\*', 'char **', c_src)
    c_src = re.sub(r'\?\s*\*',   'char *',  c_src)
    # Remaining bare ? → u32 (scalar: return types, local vars, data externs)
    c_src = re.sub(r'\?(?=[\s,);])', 'u32', c_src)
    return c_src


# Matches a top-level function definition opening line, capturing:
#   group 1 = full return type  group 2 = name  group 3 = param list (no braces)
_FN_DEF_RE = re.compile(
    r'^((?:static\s+)?[\w\s\*]+?)\s+((?:fn|dtor|ctor)_[0-9A-Fa-f]+)\s*\(([^)]*)\)\s*\{',
    re.MULTILINE
)
# Matches the stub we emitted for extern function-like names
_FN_EXTERN_RE = re.compile(
    r'^void ((?:fn|dtor|ctor)_[0-9A-Fa-f]+)\(\);$',
    re.MULTILINE
)


def write_chunk(chunk: list[Path], results: dict[Path, tuple[str, str]], out_dir: Path) -> Path:
    """Merge converted C for a chunk into one .c file. Returns output path."""
    base_addr = addr_from_name(chunk[0])
    out_file = out_dir / f"seg_{base_addr:08X}.c"

    # Pass 1: fix types and collect actual signatures of all functions defined here.
    all_fixed = {p: fix_unknown_types(results[p][0]) for p in chunk}
    # name -> "rettype name(params);"
    local_sigs: dict[str, str] = {}
    for src in all_fixed.values():
        for m in _FN_DEF_RE.finditer(src):
            ret, name, params = m.group(1).strip(), m.group(2), m.group(3).strip()
            local_sigs[name] = f"{ret} {name}({params or 'void'});"

    # Pass 2: replace generic `void fn_XXX();` stubs with the real signature
    # so forward declarations are compatible with the subsequent definition.
    def fix_local_extern(m: re.Match) -> str:
        name = m.group(1)
        return local_sigs.get(name, m.group(0))

    lines = [
        f"/* Auto-generated by tools/m2c_batch.py */",
        f"/* Address range: 0x{base_addr:08X} – 0x{addr_from_name(chunk[-1]):08X} */",
        f"#include \"nonmatch.h\"",
        "",
    ]

    for asm_path in chunk:
        stdout, stderr = results[asm_path]
        fixed = _FN_EXTERN_RE.sub(fix_local_extern, all_fixed[asm_path])
        lines.append(f"/* --- {asm_path.name} --- */")
        if stderr.strip():
            for ln in stderr.strip().splitlines():
                lines.append(f"/* m2c warning: {ln} */")
        lines.append(fixed.rstrip())
        lines.append("")

    out_file.write_text("\n".join(lines) + "\n")
    return out_file


def write_header(all_results: dict[Path, tuple[str, str]], out_dir: Path) -> None:
    """Write a header with extern declarations for all converted functions."""
    decls = []
    fn_re = re.compile(r'^(?:void|[\w\s\*]+)\s+(fn_[0-9A-Fa-f]+)\s*\(', re.MULTILINE)
    for asm_path in sorted(all_results, key=addr_from_name):
        stdout, _ = all_results[asm_path]
        stdout = fix_unknown_types(stdout)
        for m in fn_re.finditer(stdout):
            # Grab the full signature line
            start = stdout.rfind('\n', 0, m.start()) + 1
            end   = stdout.find('{', m.start())
            if end != -1:
                sig = stdout[start:end].strip()
                decls.append(f"extern {sig};")

    hdr = out_dir / "_declarations.h"
    hdr.write_text(
        "/* Auto-generated by tools/m2c_batch.py — do not edit */\n"
        "#pragma once\n"
        "#include \"nonmatch.h\"\n\n"
        + "\n".join(decls)
        + "\n"
    )
    print(f"[+] Wrote {len(decls)} declarations to {hdr.relative_to(REPO_ROOT)}")


def write_nonmatch_header(out_dir: Path) -> None:
    """Write the shared type header that all nonmatch files include."""
    hdr = out_dir / "nonmatch.h"
    if hdr.exists():
        return
    hdr.write_text(
        "/* Shared types/macros for m2c-generated nonmatching code */\n"
        "#pragma once\n\n"
        "typedef signed char        s8;\n"
        "typedef unsigned char      u8;\n"
        "typedef signed short       s16;\n"
        "typedef unsigned short     u16;\n"
        "typedef signed int         s32;\n"
        "typedef unsigned int       u32;\n"
        "typedef signed long long   s64;\n"
        "typedef unsigned long long u64;\n"
        "typedef float              f32;\n"
        "typedef double             f64;\n"
    )


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--test", type=int, metavar="N",
                        help="only convert first N asm files (for quick testing)")
    parser.add_argument("--jobs", type=int, default=os.cpu_count(),
                        help="parallel worker count (default: cpu count)")
    parser.add_argument("--out", type=Path, default=OUT_DIR,
                        help=f"output directory (default: {OUT_DIR})")
    parser.add_argument("--chunk", type=int, default=CHUNK_SIZE,
                        help=f"functions per output .c file (default: {CHUNK_SIZE})")
    args = parser.parse_args()

    out_dir = args.out
    out_dir.mkdir(parents=True, exist_ok=True)
    write_nonmatch_header(out_dir)

    if not M2C_BIN.exists():
        print(f"[!] m2c not found at {M2C_BIN}")
        print("    Run: uv tool install 'git+https://github.com/matt-kempster/m2c.git'")
        return 1

    files = find_asm_files()
    if args.test:
        files = files[:args.test]
        print(f"[test mode] converting {len(files)} functions")
    else:
        print(f"[+] Found {len(files)} asm files to convert")

    # --- parallel m2c conversion ---
    results: dict[Path, tuple[str, str]] = {}
    errors = 0
    with ProcessPoolExecutor(max_workers=args.jobs) as pool:
        futures = {pool.submit(convert_one, f): f for f in files}
        done = 0
        for fut in as_completed(futures):
            asm_path, stdout, stderr = fut.result()
            results[asm_path] = (stdout, stderr)
            done += 1
            if stderr.strip():
                errors += 1
            if done % 500 == 0 or done == len(files):
                print(f"  {done}/{len(files)} converted ({errors} with warnings)...")

    print(f"[+] Conversion done. {errors} files had m2c warnings.")

    # --- group into chunked .c files ---
    chunks = chunk_files(sorted(results.keys(), key=addr_from_name), args.chunk)
    out_files = []
    for chunk in chunks:
        out_path = write_chunk(chunk, results, out_dir)
        out_files.append(out_path)

    write_header(results, out_dir)

    print(f"[+] Wrote {len(out_files)} .c files to {out_dir.relative_to(REPO_ROOT)}/")
    print(f"    Each covers ~{args.chunk} functions.")
    print(f"    Next: review src/nonmatch/ and wire files into configure.py as NonMatching objects.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
